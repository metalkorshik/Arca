    {
        $originalRole = $user->getOriginal('role');
        if ($originalRole != $user->role)
        {
            
        }

        if ($user->rate != $user->getOriginal('rate')) {
            $history = new RateHistory(['rate' => $user->rate]);
            $history->user()->associate($user);
            $history->author()->associate(\Auth::user());
            $history->save();
        }
    }

